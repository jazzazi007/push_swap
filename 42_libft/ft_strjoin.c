#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*dest;
	size_t	s1len;
	size_t	s2len;
	size_t	i;
	size_t	j;

	i = 0;
	s1len = ft_strlen(s1);
	s2len = ft_strlen(s2);
	dest = (char *)ft_calloc((s1len + s2len + 1), sizeof(char));
	if (!dest)
		return (NULL);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j] != '\0')
	{
		dest[i + j] = s2[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
