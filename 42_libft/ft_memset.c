#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	cp;
	char	*str2;

	cp = c;
	str2 = str;
	while (n > 0)
	{
		str2[n - 1] = cp;
		n--;
	}
	return (str2);
}
