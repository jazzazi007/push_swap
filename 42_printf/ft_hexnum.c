#include "ft_printf.h"

int	ft_hexnum(unsigned int n)
{
	char			*hex;
	int				i;

	hex = "0123456789abcdef";
	i = 0;
	if (n >= 16)
		i += ft_hexnum(n / 16);
	ft_p_putchar_fd(hex[n % 16], 1);
	i++;
	return (i);
}
