#include "ft_printf.h"

int	ft_udecm(unsigned int unum)
{
	int	i;

	i = 0;
	if (unum > 9)
		i += ft_udecm(unum / 10);
	ft_p_putchar_fd(unum % 10 + 48, 1);
	i++;
	return (i);
}
